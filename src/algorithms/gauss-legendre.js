/**
 * Calculates the integral of a function using Gauss-Legendre quadrature.
 *
 * @method gaussLegendre
 * @memberof ran.algorithms
 * @param {Function} f Function to calculate the definite integral for.
 * @param {number} a Lower boundary of the integration interval.
 * @param {number} b Upper boundary of the integration interval.
 * @param {number} n Number of quadrature nodes. Must be 5, 10, or 20.
 * @return {number} The approximate integral of the function.
 * @private
 */
export default function (f, a, b, n) {
  const nodes = NODES[n]
  const weights = WEIGHTS[n]
  const c1 = 0.5 * (b - a)
  const c0 = 0.5 * (b + a)
  let s = 0
  for (let i = 0; i < n; i++) {
    s += weights[i] * f(c0 + c1 * nodes[i])
  }
  return c1 * s
}
